let LANG = localStorage.getItem('hr_lang') || 'vi';

export function getLang() { return LANG; }
export function setLang(l) {
  LANG = l === 'en' ? 'en' : 'vi';
  localStorage.setItem('hr_lang', LANG);
  document.documentElement.lang = LANG;
}
export function t(key, ...args) {
  const v = STRINGS[LANG][key] ?? STRINGS.vi[key] ?? key;
  return typeof v === 'function' ? v(...args) : v;
}
// Nhãn trường/nhóm theo ngôn ngữ hiện tại.
export function flabel(f) { return (LANG === 'en' && f.label_en) ? f.label_en : f.label; }
export function glabel(g) { return (LANG === 'en' && g.title_en) ? g.title_en : g.title; }

